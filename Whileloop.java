public class Whileloop {
    
    public static void main(String[] args) {
        int a=0;
        while(a<5){
            System.out.println(a);
            a++;
        }

        int b = 10;
        do{
            System.out.println(b);
            b++;
        }while(b<5);

        int i=0;

        for(i=0;i<=10;i++){
            System.out.println(i);

        }

    }
}
